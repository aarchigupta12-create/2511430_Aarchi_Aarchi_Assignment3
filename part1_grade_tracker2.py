# TASK 2 - MARKS ANALYSIS

student_name = "Ayesha Sharma"
subjects     = ["Math", "Physics", "CS", "English", "Chemistry"]
marks        = [88, 72, 95, 60, 78]

for i in range(len(subjects)):
    m = marks[i]

    if m >= 90:
        grade = "A+"
    elif m >= 80:
        grade = "A"
    elif m >= 70:
        grade = "B"
    elif m >= 60:
        grade = "C"
    else:
        grade = "F"

    print(subjects[i], ":", m, "-", grade)

total = sum(marks)
avg = round(total / len(marks), 2)

print("Total:", total)
print("Average:", avg)

max_marks = max(marks)
min_marks = min(marks)

print("Highest:", subjects[marks.index(max_marks)], max_marks)
print("Lowest:", subjects[marks.index(min_marks)], min_marks)

count = 0

while True:
    subject = input("Enter subject (or 'done'): ")

    if subject.lower() == "done":
        break

    marks_input = input("Enter marks: ")

    if not marks_input.isdigit():
        print("Invalid marks")
        continue

    marks_val = int(marks_input)

    if marks_val < 0 or marks_val > 100:
        print("Marks must be 0-100")
        continue

    subjects.append(subject)
    marks.append(marks_val)
    count += 1

print("New subjects added:", count)
print("Updated average:", round(sum(marks)/len(marks), 2))

#TASK 3 - CLASS SUMMARY

class_data = [
    ("Ayesha Sharma",  [88, 72, 95, 60, 78]),
    ("Rohit Verma",    [55, 68, 49, 72, 61]),
    ("Priya Nair",     [91, 85, 88, 94, 79]),
    ("Karan Mehta",    [40, 55, 38, 62, 50]),
    ("Sneha Pillai",   [75, 80, 70, 68, 85]),
]

print("Name              | Average | Status")
print("----------------------------------------")
pass_count = 0
fail_count = 0
topper_name = ""
topper_avg = 0
total_avg_sum = 0
for name, marks in class_data:
    avg = round(sum(marks)/len(marks), 2)
    total_avg_sum += avg

    if avg >= 60:
        status = "Pass"
        pass_count += 1
    else:
        status = "Fail"
        fail_count += 1

    print(f"{name:<18} | {avg:<7} | {status}")

    if avg > topper_avg:
        topper_avg = avg
        topper_name = name
print("Passed:", pass_count)
print("Failed:", fail_count)

print("Topper:", topper_name, topper_avg)

class_avg = round(total_avg_sum / len(class_data), 2)
print("Class Average:", class_avg)

